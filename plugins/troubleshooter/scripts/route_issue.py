def score(route, text):
    lowered = text.lower()
    system_area = None
    match = re.search(r"^system_area:\s*(.+?)\s*$", text, re.M)
    if match:
        system_area = SYSTEM_AREA_MAP.get(match.group(1).strip().strip("`'\""))
    corpus_match = re.search(r"^corpus:\s*(.+?)\s*$", text, re.M)
    case_corpus = corpus_match.group(1).strip().strip("`'\"") if corpus_match else None
    if case_corpus and route.get("corpus") and route["corpus"] != case_corpus:
        return 0, []
    if case_corpus == "boost-module" and route.get("corpus") != "boost-module":
        return 0, []
    if case_corpus and case_corpus != "c134" and "corpus" not in route and route["area"] in {
        "mantis.power_network",
        "mantis.load_handling",
        "ant.motion_localization",
        "ant.network",
        "ant.load_handling",
        "ant.power",
        "workstation_wled",
    }:
        return 0, []
    if system_area and route["area"] != system_area:
        return 0, []
    is_mantis_packet = "螳螂" in text or re.search(r"\bM-A\d", text)
